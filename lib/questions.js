exports.randomQuestion = function() {
    let rand = Math.floor(Math.random() * questionBank.length);
    let q = questionBank[rand];

    let query = q.query;
    let answer = q.answer;

    if (q.type === "input" && (q.answer === "t" || q.answer === "f")) {
        let translate = {t: true, f: false};
        answer = translate[answer];
    }
    return {query: query, answer: answer};
}